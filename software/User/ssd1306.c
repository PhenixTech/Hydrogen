void SSD1306_Print(uint8_t page, uint8_t col, const char *str)
{
    while (*str) {
        uint8_t c = *str++;
        if (c >= 'a' && c <= 'z') c -= 32;
        if (c < 0x20 || c > 0x5A) c = 0x20;

        SSD1306_Cmd(0xB0 + page);
        SSD1306_Cmd(0x00 + (col & 0x0F));
        SSD1306_Cmd(0x10 + (col >> 4));

        const uint8_t *glyph = font5x7[c - 0x20];
        for (uint8_t i = 0; i < 5; i++)
            SSD1306_Data(glyph[i]);
        SSD1306_Data(0x00);

        col += 6;
        if (col >= 128) break;
    }
}